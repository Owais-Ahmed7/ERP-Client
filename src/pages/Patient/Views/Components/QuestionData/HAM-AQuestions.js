export const calculateScores = (answers) => {
  let totalScore = 0;

  for (const [questionId, answerValue] of Object.entries(answers)) {
    const question = hamaQuestions.find((q) => q.id === questionId);
    if (question && question.score) {
      const scoreValue = question.score[answerValue] ?? 0;
      totalScore += scoreValue;
    }
  }

  return totalScore;
};

export const getInterpretationAndRecommendations = (subscaleScores) => {
  let severity = "";
  let interpretation = "";
  let recommendations = "";

  if (subscaleScores >= 0 && subscaleScores <= 17) {
    severity = "Mild";
    interpretation =
      "Mild anxiety symptoms, likely manageable with minimal intervention.";
    recommendations =
      "Consider non-pharmacological approaches, such as psychoeducation and lifestyle modifications. Monitor symptoms regularly.";
  } else if (subscaleScores >= 18 && subscaleScores <= 24) {
    severity = "Moderate";
    interpretation =
      "Moderate anxiety symptoms that may impact daily functioning.";
    recommendations =
      "Evaluate the need for pharmacotherapy and consider cognitive-behavioral therapy. Encourage stress management techniques.";
  } else if (subscaleScores >= 25) {
    severity = "Severe";
    interpretation =
      "Severe anxiety symptoms requiring comprehensive clinical intervention.";
    recommendations =
      "Recommend thorough psychiatric evaluation. Pharmacological treatment combined with psychotherapy is advised. Monitor closely for treatment response.";
  }

  return {
    severity,
    interpretation,
    recommendations,
  };
};
